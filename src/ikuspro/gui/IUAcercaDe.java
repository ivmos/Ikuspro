    private String getTextoSistema() {
		String mensaje = "";
		mensaje += "<html><body>";
		mensaje += mensajes.getString("Sistema_Operativo:_") + System.getProperty("os.name") + "<br/>";
		mensaje += "Java VM: " + (System.getProperty("java.runtime.version")) + "<br/>";
		mensaje += mensajes.getString("Memoria_utilizada_:_") + ((Runtime.getRuntime().totalMemory() - Runtime.getRuntime().freeMemory()) / 1000.0) + " kB <br/>";
		mensaje += "Locale: " + Locale.getDefault();


		return mensaje;
	}

